    private void refrescar() {
        ArrayList lista = controlMedicamento.listarMedicamentos();
        if (lista != null) {
            int n = lista.size();
            for (int i = 0; i < n; i++) {
                this.ComboBoxCodigoMedicamento.addItem((String) lista.get(i));
            }
        }
    }
